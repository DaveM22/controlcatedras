<template>
  <v-container fluid>
    <v-row align="center">
      <v-form ref="form" v-model="valid">
        <v-text-field
          v-model="descripcion"
          :counter="25"
          :rules="descripcionRules"
          label="Descripcion"
          required
          readonly
        ></v-text-field>

        <v-btn
          :disabled="!valid"
          @click="validate"
          color="success"
          class="mr-4"
        >
          Confirmar
        </v-btn>

        <v-btn @click="reset" color="error" class="mr-4">
          Cancelar
        </v-btn>
      </v-form>
    </v-row>
  </v-container>
</template>

<script>
import { mapActions, mapState } from 'vuex'
export default {
  data: () => ({
    valid: true,
    id: null,
    descripcion: '',
    descripcionRules: [
      (x) => !!x || 'El campo descripciÃ³n es requerido',
      (x) =>
        (x && x.length <= 25) ||
        'La descripcion de la catedra no debe superar los 25 caracteres'
    ],
    anio: 2019
  }),
  computed: mapState('catedras', ['cursoActual']),
  async mounted() {
    await this.GetCursoById(this.$route.params.id)
    this.id = this.cursoActual.id
    this.descripcion = this.cursoActual.descripcion
    this.anio = this.cursoActual.anio
  },
  methods: {
    validate() {
      if (this.$refs.form.validate()) {
        this.snackbar = true
        this.DeleteCurso(this.id)
        this.$router.push('/cursos')
      }
    },
    reset() {
      this.$router.push('/cursos')
    },
    resetValidation() {
      this.$refs.form.resetValidation()
    },
    ...mapActions('catedras', ['GetCursoById', 'DeleteCurso'])
  }
}
</script>
